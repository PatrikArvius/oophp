<h3>Spelregler tärningsspelet 101</h3>
<p>Följande är generella regler för tärningsspelet 101.</p>
<p>I det här spelet är det du mot datorn som gäller men du har möjligheten att välja hur många tärningar ni spelar med. Spelet börjar med att du och din motståndare kastar varsin tärning, den vars tärning visar högst värde börjar. Spelet kommer att föra protokoll åt dig över spelarna och målet med spelet är att vara först att nå en poängsamling av 100 poäng!</p>

<h3>Spelrunda</h3>
<p>En spelrunda inleds av en spelare genom att den kastar den mängd tärningar som valts.</p>
<p>Alla tärningar med ögon 2-6 summeras och adderas till totalen för nuvarande spelrunda. En tvåa är värd 2 poäng och en sexa är värd 6 poäng, och så vidare.
 Skulle någon utav tärningarna visa en etta så är din runda per automatik över och spelet går vidare till din motståndare. Om ingen av de kastade tärningarna visar en etta
 har du två val: spara de poäng du lyckats samla ihop på banken och ge över turen till motståndaren eller pröva din lycka genom att kasta tärningarna ännu en gång.
 Du kan fortsätta samla poäng så länge som ingen av de slagna tärningarna visar en etta men kom ihåg, har du inte sparat dina poäng på banken och du rullar en etta
 förlorar du alla osparade poäng!</p>

